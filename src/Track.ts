/**
 * Represents a track.
 */
export default class Track
{
    /**
     * The title of the track.
     */
    public title: string;

    /**
     * The artist of the track.
     */
    public artist: string;

    /**
     * The album of the track.
     */
    public album: string;

    /**
     * The URL to the artwork of the album of the track.
     */
    public albumArt: string;
}